# Задача: Декоратор для измерения времени выполнения функции
# Описание: Напишите декоратор, который будет измерять время выполнения функции и выводить его на экран. Декоратор должен принимать опциональный аргумент, который будет определять формат вывода времени (например, в секундах или миллисекундах).
# ▎Шаги для выполнения:
# 1. Создайте декоратор timeit, который принимает опциональный аргумент unit (по умолчанию "seconds").
# 2. Внутри декоратора:
#    • Запишите текущее время перед вызовом функции.
#    • Вызовите функцию и сохраните её результат.
#    • Запишите текущее время после выполнения функции.
#    • Вычислите разницу во времени.
#    • Выведите время выполнения в указанном формате:
#      • Если unit равно "seconds", выводите время в секундах.
#      • Если unit равно "milliseconds", выводите время в миллисекундах.
# 3. Примените декоратор к любой функции, например, к функции, которая выполняет какую-либо вычислительную задачу (например, вычисление факториала).

